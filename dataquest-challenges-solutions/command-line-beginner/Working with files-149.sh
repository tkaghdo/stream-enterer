## 1. Making a file ##

~$ touch test.txt

## 2. Standard streams ##

~$ echo "All bears should juggle"

## 3. Redirection ##

~$ echo "All bears should juggle" > test.txt

## 4. Editing a file ##

~$ nano test.txt

## 5. File permissions ##

~$ ls -l

## 6. Octal notation ##

~$ stat test.txt

## 7. Modifying file permissions ##

~$ chmod 0760 test.txt

## 8. Moving files ##

~$ mv test.txt test

## 9. Copying files ##

~$ cp /home/dq/test/test.txt /home/dq/test/test2.txt

## 10. File extensions ##

~$ mv test/test.txt test/test_no_extension

## 12. The root user ##

~$ sudo